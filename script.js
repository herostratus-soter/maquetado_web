// Función para el menú hamburguesa sofisticado
function toggleMenu() {
    var nav = document.getElementById('main-nav');
    var btn = document.getElementById('hamburger-btn');
    if (nav) nav.classList.toggle('active');
    if (btn) btn.classList.toggle('active');
}

// Función para cargar textos desde variable global
function cargarTextos() {
    if (typeof TEXTOS_WEB === 'undefined') return;

    var textos = TEXTOS_WEB;
    var obtenerTexto = (obj, path) => {
        return path.split('.').reduce((acc, part) => acc && acc[part], obj);
    };

    document.querySelectorAll('[data-text-id]').forEach(el => {
        var id = el.getAttribute('data-text-id');
        var texto = obtenerTexto(textos, id);
        if (texto) {
            if (texto.includes('<')) {
                el.innerHTML = texto;
            } else {
                el.textContent = texto;
            }
        }
    });
}

// Script para hacer el header sticky al hacer scroll
function initStickyHeader() {
    var header = document.querySelector("header");
    if (!header) return;

    header.classList.toggle("sticky", window.scrollY > 600);
    window.addEventListener("scroll", function () {
        header.classList.toggle("sticky", window.scrollY > 600);
    });
}

